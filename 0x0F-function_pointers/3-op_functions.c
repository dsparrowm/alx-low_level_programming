#include "3-calc.h"
/**
 * op_add - adds two integers
 * @a: first int
 * @b: second int
 *
 * Return: result
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - finds the diff between two ints
 * @a: first int
 * @b: second int
 *
 * Return: rwsult
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiplies two ints
 * @a: first int
 * @b: second int
 *
 * Return: result
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - divides two ints
 * @a: first int
 * @b: second int
 *
 * Return: result
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - returns the remainder of two ints after division
 * @a: first int
 * @b: second int
 *
 * Return: remainder
 */
int op_mod(int a, int b)
{
	return (a % b);
}
